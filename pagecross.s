# Do unaligned load/store/fetch where the second page
# triggers an exception.

        .text
        .align 6
        .globl _start
_start:


# Initialize PMP to allow all access
  li x5, 0x1f  # a=napot, x=1, w=1, r=1
  csrw pmpcfg0, x5
  li x5, -1
  csrw pmpaddr0, x5
  .align 2

# Initialize machine mode trap handler
  la x2,mtrap_handler
  csrw mtvec,x2
  la x2,mtrap_save
  csrw mscratch,x2
  li x2,0x8006300000081000
  csrw satp,x2

  # Enter supervisor mode.
  la x5, 1f
  csrw mepc, x5
  li x5, 0x00001800
  csrc mstatus, x5
  li x6, (0x00001800 & -0x00001800) * 1
  csrs mstatus, x6
  mret
1:
  la x2,mydata
  li x3,0x80003000
  li x4,0xff6
  add x4,x4,x3
  bne x4,x2,fail  # data section did not begin where I expected it to

  li x4,0x3a177dc000
  li x5,0xffe
  add x5,x5,x4
  la x31,1f  # return address
  jr x5  # should take page fault on second page, go to handler, repair access bit, execute `jr x31`
1:

  li x5,0x2ffc
  add x5,x5,x4
  ld x6,0(x5)  # should take page fault on second page, go to handler, repair access bit, load 8 bytes

  li x7,0x4ffc
  add x7,x7,x4
  li x8,-1  # value to store, clobbering everything on the page crossing
  sd x8,0(x7)  # should take page fault on second page, go to handler, repair access bit, store 8 bytes
# Handler loads from page crossing, so we can see what's there before the full store retries
  sd x6,0(x7)  # repair proper data on page crossing
  fence.i  # so next page cross fetch will see data we just stored

  ecall  # return to machine mode
2:

pass:
        li gp, 1
        sw gp, tohost, t5;

endless_loop:
        j endless_loop

        nop
        nop
        nop
        nop

testnum_failure:
        or gp, gp, 1
        sw gp, tohost, t5;
        j endless_loop

fail:
        bnez gp, testnum_failure
        li gp, -1
        sw gp, tohost, t5;
        j endless_loop

        .pushsection .tohost,"aw",@progbits
        .align 6
        .global tohost
tohost: .dword 0
        .global fromhost
fromhost: .dword 0
        .align 6
        .popsection


  .balign 4
mtrap_handler:

# We should get ifetch page fault (due to access bit).
# Then load page fault (due to access bit).
# Then store page fault (due to access bit).
# Then store page fault (due to dirty bit).

  csrrw x2,mscratch,x2
  sd x3,0(x2)  # save regs to mtrap_save region
  sd x4,8(x2)
  sd x5,16(x2)
  sd x6,24(x2)

# Load from page crossing, so we can see what (if any) got modified by
# the instruction that took the exception.
  la x3,mydata
  ld x4,2(x3)  # first half of discontiguous page crossing
  ld x5,10(x3)  # first bytes of next page (shouldn't be accessed by diag body)
  li x4,0x1000
  add x3,x3,x4
  ld x4,2(x3)  # last bytes of second page (shouldn't be accessed by diag body)
  ld x5,10(x3) # second half of discontiguous page crossing


# We will deal with the following mcause:
#  9 Environment call from S-mode
# 12 Instruction page fault
# 13 Load page fault
# 14 Reserved for future standard use
# 15 Store/AMO page fault
  csrr x3,mcause
  li x4,~3  # clear lower two bits, so we can detect 12/13/14/15 all at once
  and x4,x4,x3
  li x5,0xc
  beq x4,x5,mtrap_page_fault
  li x5,9  # ecall
  beq x3,x5,mtrap_ecall
# Unexpected exception:
  j fail

mtrap_page_fault:
# Patch up the failing PTE, by setting the access
# bit (and dirty bit, if it's a store).

  csrr x4,satp
  li x5,(1 << 22) - 1  # satp has 22 bits of ppn
  and x5,x4,x5
  slli x5,x5,12  # x5 = base of first-level page table
  csrr x6,mtval
  srli x6,x6,27  # put Sv39 VPN[2] into proper bits
  li x3,0x1ff << 3  # 9 bit mask for VPN[2]
  and x6,x6,x3
  or x6,x6,x5  # construct address for first-level page walk
  ld x5,0(x6)  # load first-level pte
  andi x6,x5,0xf  # isolate x/w/r/v bits
  li x4,1
  bne x6,x4,fail  # I expect x=0 w=0 r=0 v=1 for first-level pte
  # Isolate bits [53:10] for PPN, then shift it left by 2, then mux in VPN[1]
  li x3,((1 << 44) - 1) << 10
  and x5,x3,x5
  slli x5,x5,2
  csrr x6,mtval
  srli x6,x6,18  # put Sv39 VPN[1] into proper bits
  li x3,0x1ff << 3  # 9 bit mask for VPN[1]
  and x6,x6,x3
  or x6,x6,x5  # construct address for second-level page walk
  ld x5,0(x6)  # load second-level pte
  andi x6,x5,0xf  # isolate x/w/r/v bits
  li x4,1
  bne x6,x4,fail  # I expect x=0 w=0 r=0 v=1 for second-level pte
  # Isolate bits [53:10] for PPN, then shift it left by 2, then mux in VPN[0]
  li x3,((1 << 44) - 1) << 10
  and x5,x3,x5
  slli x5,x5,2
  csrr x6,mtval
  srli x6,x6,9  # put Sv39 VPN[0] into proper bits
  li x3,0x1ff << 3  # 9 bit mask for VPN[0]
  and x6,x6,x3
  or x6,x6,x5  # construct address for third-level page walk
  ld x5,0(x6)  # load third-level pte
  li x3,0x40  # access bit
  and x4,x5,x3
  bne x4,x0,check_dirty
  or x5,x5,x3  # set access bit
  j write_pte
check_dirty:
  li x3,0x80  # dirty bit
  and x4,x5,x3
  bne x4,x0,fail  # if it wasn't the dirty bit, fail, because we don't handle anything else here
  or x5,x5,x3  # set dirty bit
write_pte:
  sd x5,0(x6)  # write to third-level pte
  csrr x6,mtval
  sfence.vma x6,x0
  j mtrap_return


mtrap_ecall:
# Return to machine mode following the ecall instruction.
  csrr x3,mepc
  li x4,4  # +4 to skip ecall
  add x3,x4,x3
  csrw mepc,x3
  li x4,0x1800  # mstatus.MPP field
  csrs mstatus,x4  # so we return to machine mode
  j mtrap_return

mtrap_return:
  ld x6,24(x2)
  ld x5,16(x2)
  ld x4,8(x2)
  ld x3,0(x2)
  csrrw x2,mscratch,x2
  mret

  .section .data
  .balign 8
mtrap_save:
  .fill 4,8,0x0000000000000000  # repeat, size, value

  .org 0x0ff6  # so that third instruction is on page crossing, and there's at least 8 bytes of data for load/stores
  .option norvc
mydata:
  and x1,x2,x0  # never executed
  add x3,x4,x5  # never executed
  jr x1  # only half this instruction is fetched...the x1 here + x30 in the next page combine to make this "jr x31"
  sub x7,x8,x9  # never executed
  or x10,x11,x12  # never executed

# first chunk here should never be accessed by diag body; after page crossing in the middle of the JR instruction, it will be.
  .org 0x1ff6
  and x11,x12,x10  # never executed
  add x13,x14,x15  # never executed
  jr x30  # returns back to caller (actually x31 because only half this instruction gets fetched)
  sub x17,x18,x19  # never executed
  or x20,x21,x22  # never executed

